package wrapper;

public class WrapperMethod {
    public static void main(String[] args) {
        System.out.println(Integer.MAX_VALUE);     //输出Integer类的最大值
        System.out.println(Integer.MIN_VALUE);      //输出Integer类的最小值

        System.out.println(Character.isDigit('1'));   //判断是不是数字
        System.out.println(Character.isLetter('a'));     //判断是不是字母
        System.out.println(Character.isUpperCase('A'));      //判断是不是大写字母
        System.out.println(Character.isLowerCase('a'));    //判断是不是小写字母

    }
}
